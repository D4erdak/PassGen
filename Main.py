"""
Главный модуль приложения "Генератор паролей".

Этот модуль является точкой входа в приложение. Он создает главное окно
и запускает основной цикл обработки событий Tkinter.
"""

import tkinter as tk
from gui.MainWindow import MainWindow


def main() -> None:
    """
    Основная функция приложения.

    Создает корневое окно Tkinter, инициализирует главное окно приложения
    и запускает основной цикл обработки событий.

    Процесс работы:
    1. Создание корневого окна Tkinter
    2. Инициализация главного окна приложения
    3. Запуск основного цикла обработки событий
    """
    try:
        # Инициализация корневого окна
        root = tk.Tk()

        # Настройка параметров окна
        root.title("Генератор паролей")  # Устанавливается в MainWindow
        root.geometry("800x600")

        # Создание и отображение главного окна приложения
        MainWindow(root)

        # Запуск основного цикла обработки событий
        root.mainloop()

    except Exception as e:
        # Обработка критических ошибок инициации приложения
        print(f"Ошибка запуска приложения: {str(e)}")
        raise


if __name__ == "__main__":
    # Запуск приложения только при прямом вызове скрипта
    main()